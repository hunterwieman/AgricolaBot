#!/usr/bin/env python
"""Export the RESTRICTED fence universe (109 entries) to a generated C++ file.

The enumeration logic in agricola/fences.py is NOT ported to C++; instead we
emit the precomputed PastureCandidate table here, in the SAME order as the
Python tuple (UNIVERSE_RESTRICTED_ENTRIES — load-bearing order, since the
fencing policy head's class indices and the legality scan both depend on it).

Run from the repo root:
    ~/miniconda3/bin/python cpp/gen/export_fence_universe.py

Writes cpp/src/fence_universe_data.cpp. Re-run if fences.py ever changes.
"""
from __future__ import annotations

import pathlib
import sys

# Make `agricola` importable regardless of cwd (this script lives in cpp/gen/,
# so Python puts cpp/gen on sys.path, not the repo root).
sys.path.insert(0, str(pathlib.Path(__file__).resolve().parents[2]))

from agricola.fences import UNIVERSE_RESTRICTED_ENTRIES

OUT = pathlib.Path(__file__).resolve().parent.parent / "src" / "fence_universe_data.cpp"

HEADER = """\
// GENERATED by cpp/gen/export_fence_universe.py — DO NOT EDIT BY HAND.
//
// The RESTRICTED fence universe (UNIVERSE_RESTRICTED_ENTRIES from
// agricola/fences.py), in the same order as the Python tuple. Each entry is a
// PastureCandidate: cells_bm / h_boundary_bm / v_boundary_bm / adjacency_bm
// (the precomputed edge metadata) + the sorted cell list.
#include "agricola/fences.hpp"

namespace agricola {

const std::vector<PastureCandidate>& restricted_universe_entries() {
  static const std::vector<PastureCandidate> kEntries = {
"""

FOOTER = """\
  };
  return kEntries;
}

}  // namespace agricola
"""


def main() -> None:
    lines = [HEADER]
    for e in UNIVERSE_RESTRICTED_ENTRIES:
        cells = sorted(e.cells)
        cells_str = ", ".join(f"{{{r},{c}}}" for (r, c) in cells)
        lines.append(
            f"      PastureCandidate{{{e.cells_bm}, {e.h_boundary_bm}, "
            f"{e.v_boundary_bm}, {e.adjacency_bm}, {{{cells_str}}}}},\n"
        )
    lines.append(FOOTER)
    OUT.write_text("".join(lines))
    print(f"Wrote {len(UNIVERSE_RESTRICTED_ENTRIES)} entries to {OUT}")


if __name__ == "__main__":
    main()
